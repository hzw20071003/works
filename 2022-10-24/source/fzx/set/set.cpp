const LL kM = 1e9 + 7;

int n, c;

int main() {
  RF("set");
  ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  cin >> n;
  for (unsigned int i = 1; i <= (1 << n + 1) - 1; ++i) {
    bool f = 1;
    for (unsigned int j = 0; f && j <= n; ++j) {
      for (unsigned int k = 0; f && k <= n; ++k) {
        if ((i >> j & 1) && (i >> k & 1) &&
            (!(i >> (j ^ k) & 1) || (j ^ k) > n)) {
          f = 0;
          break;
        }
      }
    }
    c += f;
  }
  cout << c;
  return 0;
}