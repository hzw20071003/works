#define RF(s) freopen(s".in", "r", stdin), freopen(s".out", "w", stdout)

using namespace std;
using LL = long long;
using Pii = pair<int, int>;
using Pll = pair<LL, LL>;

const int kN = 1e5 + 1;

int n, a[kN], d[kN];
bool v[kN];
LL s;

int gcd(int x, int y) { return y ? gcd(y, x % y) : x; }

int main() {
  RF("gcd");
  ios_base::sync_with_stdio(0), cin.tie(0), cout.tie(0);
  cin >> n;
  for (int i = 1; i <= n; ++i) {
    cin >> a[i];
  }
  for (int i, j = 1; j <= n; ++j) {
    i = 0;
    for (int k = 1; k <= n; ++k) {
      if (!v[k] && (!i || d[i] < d[k])) {
        i = k;
      }
    }
    v[i] = 1, s += d[i];
    for (int k = 1; k <= n; ++k) {
      if (!v[k]) {
        d[k] = max(d[k], gcd(a[i], a[k]));
      }
    }
  }
  cout << s;
  return 0;
}